function soma (num1, num2) {
    return num1 + num2;
}

function subtracao (num1, num2) {
    return num1 - num2;
}
function multiplica (num1, num2) {
    return num1 * num2;
}
function divide (num1, num2) {
    return num1 / num2;
}

exports.soma = soma;
exports.subtracao = subtracao;
exports.multiplica = multiplica;
exports.divide = divide;
